# Для прикладу використання break -
# написати программу, яка шукає заданне ім'я у списку,
# і якщо знаходить виводить - “Значення <ім'я >знайдено”, якщо ж значення не знайдене виводить “Такого користувачу тут немає”

list = ["Ксенія", "Георгій", "Данило", "Міша", "Голголіно"]
while True:
    name = input("Введіть ім'я, яке ви хочете знайти: ")
    if name in list:
        print(f"Значення {name} знайдено!")
    else:
        print("Такого користувачу тут немає")
        break